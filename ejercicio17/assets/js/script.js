/*
 * INCREMENT = i o j
 * DECREMENT = d
 * COUNT = c
 */

for (let i = 0; i <= 10; i++) {
  document.write(`El valor de i es: ${i} <br>`);
}

for (let d = 10; d >= 0; d--) {
  document.write(`El valor de d es: ${d} <br>`);
}

let c;
for (c = 0; c <= 10; c++);
document.write(`El valor de c es: ${c} <br>`);
