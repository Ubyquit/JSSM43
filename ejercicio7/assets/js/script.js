let a = 2;
let b = parseInt(prompt("Ingresa el valor de b"));

document.write(`${a} + ${b} = ${a + b} <br>`);
document.write(`${a} - ${b} = ${a - b} <br>`);
document.write(`${a} * ${b} = ${a * b} <br>`);
document.write(`${a} / ${b} = ${a / b} <br>`);
document.write(`${a} ** ${b} = ${a ** b} <br>`);
document.write(Math.sqrt(a)); // Raiz cuadrada