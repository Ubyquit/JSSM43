function obtenerNumero(argumento) {

  if (argumento == 6) {
    document.write( argumento + " es igual a 6 <br> ");
  } else {
    document.write(argumento + " es diferente a 6 <br> ");
  }
}

obtenerNumero(4);
obtenerNumero(5);
obtenerNumero(6);
obtenerNumero(7);
obtenerNumero("Hola mundo");
